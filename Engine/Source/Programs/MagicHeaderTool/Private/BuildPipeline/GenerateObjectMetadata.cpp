        /**
         * 生成序列化函数
         */
        std::string GenerateSerializeFunctionFormatted(
            const std::map<std::string, std::vector<MemberVariable>>& classParamsMap,
            const std::string& templateArg = "Archive"
        ) {
            std::ostringstream code;

            code << "template<class " << templateArg << ">\n";
            code << "void serialize(" << templateArg << "& archive) {\n";
            code << "    using ThisType = std::decay_t<decltype(*this)>;\n";
            code << "    \n";

            bool firstClass = true;
            for (const auto& [className, params] : classParamsMap) {
                if (!firstClass) {
                    code << "    } else ";
                }
                code << "if constexpr (std::is_same_v<ThisType, " << className << ">) {\n";
                code << "        archive(";
                for (size_t i = 0; i < params.size(); ++i) {
                    const MemberVariable& Variable = params[i];
                    code << Variable.name;
                    if (i < params.size() - 1) {
                        code << ", ";
                    }
                }
                code << ");\n";
                firstClass = false;
            }

            if (!classParamsMap.empty()) {
                code << "    }\n";
            }
            code << "}\n";
            return code.str();
        }

        std::string ConvertFunctionToMacro(const std::vector<std::string>& funcStrings, const std::string& macroName = "GENERATE_BODY") {
            std::ostringstream result;
            result << "#define " << macroName << "() \\\n";

            bool firstLine = true;

            for (const auto& funcString : funcStrings) {
                std::stringstream ss(funcString);
                std::string line;

                while (std::getline(ss, line)) {
                    // 去除换行符和首尾空白
                    line.erase(std::remove(line.begin(), line.end(), '\r'), line.end());

                    size_t start = line.find_first_not_of(" \t");
                    if (start == std::string::npos) continue;

                    line = line.substr(start);

                    if (!firstLine) {
                        result << " \\\n";
                    }

                    result << "    " << line;
                    firstLine = false;
                }
            }

            return result.str();
        }

        std::string GenerateFileWithCode(
            const std::vector<std::string>& codeLines,
            const std::string& generatorName = "MagicHeaderTool",
            const std::string& additionalInfo = ""
        ) {
            std::ostringstream result;

            // 生成文件头部
            result << "#pragma once\n\n";
            result << "// ============================================================================\n";
            result << "// This file is generated by " << generatorName << ".\n";
            result << "// DO NOT MODIFY THIS FILE MANUALLY!\n";
            result << "// Any manual changes will be overwritten.\n";

            if (!additionalInfo.empty()) {
                result << "// \n";
                result << "// " << additionalInfo << "\n";
            }

            result << "// ============================================================================\n\n";

            // 追加用户代码
            for (const auto& line : codeLines) {
                result << line << "\n";
            }

            return result.str();
        }
