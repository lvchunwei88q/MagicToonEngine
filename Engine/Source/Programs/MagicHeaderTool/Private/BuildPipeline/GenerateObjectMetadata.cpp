        struct MetaData {
            std::string headerName;
            std::string MetadataSrc;
            std::string Metadata;

            void SetMetadataSrc(const std::string& MetadataSrc, const std::string& headerName)
            {
                this->MetadataSrc = MetadataSrc;
                this->headerName  = headerName;
            }
            void SetMetadata(const std::string& Metadata) { this->Metadata = Metadata; }
        };

		bool GenerateObjectMetadata() {
            const auto& MagicEngineClasss = MagicBuildData::Get().GetRefMagicEngineClasss();
            auto& MagicObjectMetadatas = MagicBuildData::Get().GetRefMagicObjectMetadatas();
            std::unordered_set<std::string> AllArea;
            std::vector<MetaData> MetaDatas;

            // 获取所有不重复的区域
            for (size_t i = 0; i < MagicEngineClasss.size(); i++)
            {
                auto& MagicEngineClass = MagicEngineClasss[i];
                AllArea.insert(MagicEngineClass.headerName);
            }

            // 为每个文件生成对应的生成信息 之后生成元数据
            for (const auto& Area : AllArea) 
            {
                std::map<std::string, std::vector<MemberVariable>> GenerateInformationMap;
                for (size_t y = 0; y < MagicEngineClasss.size(); y++)
                {
                    auto& MagicEngineClass = MagicEngineClasss[y];
                    if (Area == MagicEngineClass.headerName) {
                        GenerateInformationMap[MagicEngineClass.className] = MagicEngineClass.members;
                    }
                }
                // 生成元数据
                std::string src = GenerateSerializeFunctionFormatted(GenerateInformationMap);
                MetaDatas.emplace_back().SetMetadataSrc(src, Area);
            }

            // 之后将我们生成的元数据使用宏包裹
            for (size_t i = 0; i < MetaDatas.size(); i++)
            {
                auto& MetaData = MetaDatas[i];
                std::string MetaDataScript = ConvertFunctionToMacro({ MetaData.MetadataSrc });
                MetaData.SetMetadata(MetaDataScript);
            }

            // 之后生成文件头与解释说明
            for (size_t i = 0; i < MetaDatas.size(); i++)
            {
                auto& MetaData = MetaDatas[i];
                std::string MetaDataScript = GenerateFileWithCode({ MetaData.Metadata });
                MetaData.SetMetadata(MetaDataScript);
            }

            // 最后把数据给MagicBuildData
            for (size_t i = 0; i < MetaDatas.size(); i++)
            {
                auto& MetaData = MetaDatas[i];

                std::string ModelName = "NOT";
                for (size_t y = 0; y < MagicEngineClasss.size(); y++)
                {
                   const auto& MagicEngineClass = MagicEngineClasss[y];
                    if (MagicEngineClass.headerName == MetaData.headerName) {
                        ModelName = MagicEngineClass.moudelName;
                    }
                }

                MagicObjectMetadatas.push_back({ MetaData.headerName , MetaData.Metadata,ModelName });
            }

			return true;
		}
